public class Tm {

    private int m;
    private String y;
    public Tm(int m, String y) {
        this.m = m;
        this.y = y;
    }
    public int getM() {
        return m;
    }
    public void setM(int m) {
        this.m = m;
    }
    public String getY() {
        return y;
    }
    public void setY(String y) {
        this.y = y;
    }
    
}
